@RestControllerAdvice
public class RESTControllerLoggingAdvice implements ResponseBodyAdvice<Object> {

	private static final Logger logger = LoggerFactory.getLogger(RESTControllerLoggingAdvice.class);

	@Override
	public boolean supports(MethodParameter returnType, Class<? extends HttpMessageConverter<?>> converterType) {
		return true;
	}

	@Override
	public Object beforeBodyWrite(Object body, MethodParameter returnType, MediaType selectedContentType,
			Class<? extends HttpMessageConverter<?>> selectedConverterType, ServerHttpRequest request,
			ServerHttpResponse response) {
		List<MediaType> accept = request.getHeaders().getAccept();
		logger.info("Requested ContentType - {}, Selected ContentType - {}, Converter used - {}", accept,
				selectedContentType, selectedConverterType.getSimpleName());
		return body;
	}

}
