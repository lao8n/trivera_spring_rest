@Configuration
@EnableWebMvc
public class RESTConfig implements WebMvcConfigurer {

	@Override
	public void configureContentNegotiation(ContentNegotiationConfigurer configurer) {
		configurer.defaultContentType(MediaType.APPLICATION_JSON).ignoreAcceptHeader(false).favorParameter(true)
				.parameterName("media");
	}

}
